    printf("O número %d é divisível por 5" . "\n" , $num);
} else {
    printf("O número %d não é divisível por 5" . "\n" , $num);
}
if($num % 2 == 0) {
    printf("O número %d é divisível por 2" . "\n" , $num);
} else {
    printf("O número %d não é divisível por 2" . "\n" , $num);
}